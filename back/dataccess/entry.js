const getAll = (filter) => {
    let filtrado = Catalogo;

    if (filter.nombre) {
        filtrado = filtrado.filter(e => e.nombre === filter.nombre)
    }

    if (filter.generos) {
        filtrado = filtrado.filter(e => e.generos.includes(filter.generos))
    }

    if (filter.buscar) {
        filtrado = filtrado.filter(e => e.nombre.includes(filter.buscar) ||
            e.descripcion.includes(filter.buscar) || e.generos.includes(filter.buscar) ||
            e.desarrollador.includes(filter.buscar) || e.editor.includes(filter.buscar))
    }

    if (filter.busquedas) {
        const palabrasABuscar = filter.busquedas.split(',');
        filtrado = filtrado.filter(entry => {
            const filtro = palabrasABuscar.filter(palabra => entry.nombre.includes(palabra) ||
                entry.generos.includes(palabra) || entry.descripcion.includes(palabra) ||
                entry.desarrollador.includes(palabra))
            return filtro.length > 0
        })
    }
    return filtrado
};

const getOne = (id) => { return Catalogo.find((registro) => registro.id == id); }

const save = (body) => { Catalogo.push(body); }

const borrar = (id) => {
    const index = Catalogo.findIndex((registro) => registro.id == id);
    if (index >= 0) {
        Catalogo.splice(index, 1);
        return true
    }
    return false
}

const update = (id, body) => {
    const index = Catalogo.findIndex((registro) => registro.id == id);
    if (index >= 0) {
        Catalogo[index] = body;
        return true
    }
    return false
}

module.exports = { getAll, getOne, save, borrar, update };